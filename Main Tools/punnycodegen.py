def generate_punycode_variants(letter):
    print(f"\nğŸ” Punycode variants for letter: '{letter}'\n")
    letter = letter.lower()
    glyphs = homoglyphs_map.get(letter, [])
    if not glyphs:
        print("âŒ No homoglyphs found for this letter.")
        return

    for glyph in glyphs:
        punycode = encode_punycode(glyph)
        if punycode:
            print(f"{glyph} -> {punycode}")

